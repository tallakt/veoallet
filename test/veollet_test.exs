  @private Veollet.hex_to_bin("b81fd29207c813fcdeeb634bfe344e686a20839d5c25b3528d176c2c684c654d")
  @public Base.decode64!(
            "BCaMhtR+G7SLD+s0ZP7J7O3jwu43wZK+3FP/wPxsNcYd80acbkJj3o8tZ8yXFV4OXMxRKqQ4cy6QyCkJtR6U2f4="
          )

  def verify_signed_tx?({:signed, tx, signature, _}, public_key) do
    opts = [public_key, :crypto.ec_curve(:secp256k1)]
    :crypto.verify(:ecdsa, :sha256, Veollet.serialize(tx), Base.decode64!(signature), opts)
  end

